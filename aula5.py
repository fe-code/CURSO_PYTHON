# Tipo de dado bool (boolean)
# Ao questionar algo em algum programa
# só existe duas respostas possiveis:
# sim (True) ou não (False)
# existem varios operadores para "questionar"
# Dentre eles o ==, que é um operador lógico que
# questiona se um valor é igual ao outro

print(10 == 10) # Sim => True (verdadeiro)
print(10 == 11) # Não => False (falso)
print(type(10 == 11)) 